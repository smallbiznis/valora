CREATE TABLE IF NOT EXISTS countries (
  code            CHAR(2) PRIMARY KEY,
  name            TEXT NOT NULL,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS timezones (
  name            TEXT PRIMARY KEY,  -- IANA timezone name, e.g. Asia/Jakarta
  region          TEXT NOT NULL,      -- e.g. Asia, Europe, America, Etc
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS country_timezones (
  country_code    CHAR(2) NOT NULL,
  timezone_name   TEXT NOT NULL,
  PRIMARY KEY (country_code, timezone_name),
  CONSTRAINT fk_country_timezones_country
    FOREIGN KEY (country_code) REFERENCES countries(code) ON DELETE CASCADE,
  CONSTRAINT fk_country_timezones_timezone
    FOREIGN KEY (timezone_name) REFERENCES timezones(name) ON DELETE CASCADE
);

